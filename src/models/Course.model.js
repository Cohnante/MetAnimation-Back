const {Schema,model} = require('mongoose')

const CourseSchema = new Schema({
  Name:{
    Type:String
  },
  Description:{
    Type:String
  },
  Duration:{
    Type:Number
  },
  IdTeacher:{
    Type:Number
  },
  Lenguaje:{
    Type:String
  },
  Category:{
    Type:String
  },
  Details:[{
    Name:{
      Type:String
    },
    State:{
      type:Boolean
    },
    Url:{
      type:String
    }
  }],
  Modules:[{
    Name:{
      String
    },
    Clase:{
      String
    },
    Url:{
      Type:String
    }
  }],
  Files:[{
    Name:{
      type:String
    },
    State:{
      Type:Boolean
    },
    URl:{
      type:String
    }
  }],
  ClasesContent:[{
    Duration:{
      Type:Number
    },
    Description:{
      Type:String
    },
    Url:{
      Type:String
    },
    Status:{
      type:Boolean
    }
  }],
  Links:[{
    Module:{
      type:String
    },
    Name:{
      Type:String
    },
    State:{
      Type:Boolean
    },
    Url:{
      Type:String
    }
  }]
})

module.exports = model('Cursos',CourseSchema)